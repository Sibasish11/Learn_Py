# Introduction
print(3 + 2)   # addition(+)
print(3 - 2)   # subtraction(-)
print(3 * 2)   # multiplication(*)
print(3 / 2)   # division(/)
print(3 ** 2)  # exponential(**)
print(3 % 2)   # modulus(%)
print(3 // 2)  # Floor division operator(//)

# data types

print(type(10))                  # Int type
print(type(3.14))                # Float type
print(type(1 + 3j))              # Complex type 
print(type('Asabeneh'))          # String type
print(type([1, 2, 3]))           # List type  
print(type({'name':'Asabeneh'})) # Dictionary type
print(type({9.8, 3.14, 2.7}))    # Set type 
print(type((9.8, 3.14, 2.7)))    # Tuple type
print(type(3 == 3))              # Bool type
print(type(3 >= 3))              # Bool type
